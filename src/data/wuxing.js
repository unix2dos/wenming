// Note: 明/晨 appear in multiple categories in different traditions.
// We follow the majority convention: 明→水(from 日+月, 月=水), 晨→金(from 辰+日)
// When a character has disputed classification, the lookup still returns one answer.

/**
 * 查询单个汉字的五行属性
 * @param {string} char - 单个汉字
 * @returns {string|null} '金'|'木'|'水'|'火'|'土' 或 null（查不到）
 */
export function lookupWuxing(char) {
  if (!char || char.length !== 1) return null;
  return WUXING_MAP[char] ?? null;
}

/**
 * 查询名字中每个字的五行
 * @param {string} name - 名字（不含姓）
 * @returns {Array<{char: string, element: string|null}>}
 */
export function analyzeNameWuxing(name) {
  if (!name) return [];
  return [...name].map(char => ({
    char,
    element: lookupWuxing(char),
  }));
}

/**
 * 五行相生相克关系
 */
const WUXING_SHENG = { 木: '火', 火: '土', 土: '金', 金: '水', 水: '木' }; // A 生 B
const WUXING_KE = { 木: '土', 土: '水', 水: '火', 火: '金', 金: '木' };   // A 克 B

/**
 * 判断两个五行之间的关系
 * @returns {'相生'|'被生'|'相克'|'被克'|'同行'|null}
 */
export function wuxingRelation(a, b) {
  if (!a || !b) return null;
  if (a === b) return '同行';
  if (WUXING_SHENG[a] === b) return '相生';  // a 生 b
  if (WUXING_SHENG[b] === a) return '被生';  // b 生 a
  if (WUXING_KE[a] === b) return '相克';     // a 克 b
  if (WUXING_KE[b] === a) return '被克';     // b 克 a
  return null;
}
