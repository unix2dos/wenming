const TIANGAN = ['甲', '乙', '丙', '丁', '戊', '己', '庚', '辛', '壬', '癸'];
const DIZHI = ['子', '丑', '寅', '卯', '辰', '巳', '午', '未', '申', '酉', '戌', '亥'];
const ZODIAC = ['鼠', '牛', '虎', '兔', '龙', '蛇', '马', '羊', '猴', '鸡', '狗', '猪'];

/**
 * 根据公历日期获取农历年份
 * 关键：处理农历新年边界——公历 1 月/2 月出生的人可能属于上一年
 * @param {string} dateStr - 'YYYY-MM-DD' 格式
 * @returns {number|null} 农历年份
 */
export function getLunarYear(dateStr) {
  if (!dateStr) return null;
  const [year, month, day] = dateStr.split('-').map(Number);
  if (!year || !month || !day) return null;

  const nyData = LUNAR_NEW_YEAR[year];
  if (!nyData) return null; // 超出查找表范围

  const [nyMonth, nyDay] = nyData;

  // 如果生日在该年农历新年之前，属于上一年
  if (month < nyMonth || (month === nyMonth && day < nyDay)) {
    return year - 1;
  }
  return year;
}

/**
 * 获取天干地支
 * 天干：(年 - 4) % 10 → 甲乙丙丁戊己庚辛壬癸
 * 地支：(年 - 4) % 12 → 子丑寅卯辰巳午未申酉戌亥
 * @param {number} lunarYear - 农历年份
 * @returns {{ tiangan: string, dizhi: string, full: string }|null}
 */
export function getTianganDizhi(lunarYear) {
  if (!lunarYear) return null;
  const tg = TIANGAN[(lunarYear - 4) % 10];
  const dz = DIZHI[(lunarYear - 4) % 12];
  return { tiangan: tg, dizhi: dz, full: `${tg}${dz}` };
}

/**
 * 获取生肖
 * @param {number} lunarYear - 农历年份
 * @returns {{ animal: string, dizhi: string }|null}
 */
export function getZodiac(lunarYear) {
  if (!lunarYear) return null;
  const idx = (lunarYear - 4) % 12;
  return { animal: ZODIAC[idx], dizhi: DIZHI[idx] };
}

/**
 * 从公历日期获取完整的农历信息
 * @param {string} dateStr - 'YYYY-MM-DD' 格式
 * @returns {{ lunarYear: number, tiangan: string, dizhi: string, tianganDizhi: string, zodiac: string }|null}
 */
export function getLunarInfo(dateStr) {
  const lunarYear = getLunarYear(dateStr);
  if (!lunarYear) return null;

  const tgdz = getTianganDizhi(lunarYear);
  const zodiac = getZodiac(lunarYear);

  return {
    lunarYear,
    tiangan: tgdz.tiangan,
    dizhi: tgdz.dizhi,
    tianganDizhi: tgdz.full,
    zodiac: zodiac.animal,
  };
}
